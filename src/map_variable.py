import colorsys, random

def random_flower_color():
    h = random.uniform(0, 1)
    s = 0.9  # Saturation
    v = 1.0  # Value/Brightness
    r, g, b = colorsys.hsv_to_rgb(h, s, v)
    return (r, g, b, 1)

